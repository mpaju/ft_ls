char	*ft_strrchr(char *str, int c)
{
	char	ch;
	char	*ret;

	ch = (char)c;
	ret = 0;
	while (*str || *str == ch)
	{
		if (*str == ch)
		{
			ret = str;
			if (!ch)
				return (ret);
		}
		str++;
	}
	return (ret);
}
